package linkedlists;

class Node	{
	int data;
	Node next;
	Node(int data)	{
		this.data = data;
	}
	
	public Node getNext()	{
		return next;
	}
	
	public int getData()	{
		return data;
	}
	
	public void setData(int data) {
		this.data = data;
	}

	public void setNext(Node next) {
		this.next = next;
	}
}
